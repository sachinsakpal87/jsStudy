let val;

val = Math.round(2.456);        //2
console.log(val);

val = Math.ceil(2.456);         //3
console.log(val)

val = Math.floor(2.456);        //2
console.log(val)

val = Math.sqrt(64);
console.log(val)

val = Math.pow(8,2);
console.log(val)

val = Math.abs(-3);
console.log(val)

val = Math.min(3,2,77,99,-76);  //-76
console.log(val)

val = Math.max(3,2,77,99,-76);  //99
console.log(val)

val = Math.random();
console.log(val)

val = Math.random() * 20; //multiple by max number which you want so the range is now 0-19
